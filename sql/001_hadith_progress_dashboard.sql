-- =============================================================================
-- 10. View: user_daily_reading_activity
--     Hadiths read per UTC day per user. Respects RLS via security_invoker.
-- =============================================================================
DROP VIEW IF EXISTS public.user_daily_reading_activity CASCADE;
CREATE VIEW public.user_daily_reading_activity
WITH (security_invoker = on) AS
SELECT
  user_id,
  (read_at AT TIME ZONE 'UTC')::date AS day,
  COUNT(*)::integer                  AS hadiths_read
FROM public.hadith_progress
GROUP BY user_id, (read_at AT TIME ZONE 'UTC')::date;

COMMENT ON VIEW public.user_daily_reading_activity IS
  'Per-user per-day read count. security_invoker=on so RLS limits to own rows.';


-- =============================================================================
-- 11. View: user_collection_progress
--     Per-user per-book completion. Joins hadith_progress with public.hadiths.
-- =============================================================================
DROP VIEW IF EXISTS public.user_collection_progress CASCADE;
CREATE VIEW public.user_collection_progress
WITH (security_invoker = on) AS
WITH book_totals AS (
  SELECT
    book_number,
    COUNT(*)::integer AS total
  FROM public.hadiths
  GROUP BY book_number
)
SELECT
  hp.user_id,
  h.book_number,
  MAX(h.book_name_en)                         AS book_name_en,
  MAX(h.book_name_ar)                         AS book_name_ar,
  COUNT(DISTINCT hp.hadith_id)::integer       AS hadiths_read,
  bt.total                                    AS total_in_book,
  ROUND(
    100.0 * COUNT(DISTINCT hp.hadith_id) / NULLIF(bt.total, 0),
    1
  )::numeric(5,1)                             AS percent_complete
FROM public.hadith_progress hp
JOIN public.hadiths h    ON h.id = hp.hadith_id
JOIN book_totals bt      ON bt.book_number = h.book_number
GROUP BY hp.user_id, h.book_number, bt.total;

COMMENT ON VIEW public.user_collection_progress IS
  'Per-user per-book completion %. security_invoker=on so caller sees own rows only.';


-- =============================================================================
-- 12. View: user_leaderboard
--     Global per-user ranking. All authenticated users see all rows.
-- =============================================================================
DROP VIEW IF EXISTS public.user_leaderboard CASCADE;
CREATE VIEW public.user_leaderboard
WITH (security_invoker = on) AS
SELECT
  ROW_NUMBER() OVER (ORDER BY us.points DESC, us.hadiths_read DESC, us.user_id ASC)::integer
                                                AS rank,
  us.user_id,
  COALESCE(up.display_name, 'Anonymous Reader') AS display_name,
  up.avatar_url,
  up.company,
  us.hadiths_read,
  us.points,
  us.coins,
  us.trophies,
  us.streak_days,
  us.level
FROM public.user_stats us
LEFT JOIN public.user_profiles up ON up.id = us.user_id;

COMMENT ON VIEW public.user_leaderboard IS
  'Global leaderboard. Uses user_stats RLS (authenticated read=true).';


-- =============================================================================
-- 13. View: company_leaderboard
--     Company-level totals derived from user_profiles.company.
-- =============================================================================
DROP VIEW IF EXISTS public.company_leaderboard CASCADE;
CREATE VIEW public.company_leaderboard
WITH (security_invoker = on) AS
SELECT
  ROW_NUMBER() OVER (ORDER BY SUM(us.points) DESC, SUM(us.hadiths_read) DESC, up.company ASC)::integer
                                  AS rank,
  up.company,
  COUNT(DISTINCT us.user_id)::integer AS members,
  SUM(us.hadiths_read)::integer       AS total_hadiths,
  SUM(us.points)::integer             AS total_points,
  SUM(us.trophies)::integer           AS total_trophies
FROM public.user_stats us
JOIN public.user_profiles up ON up.id = us.user_id
WHERE up.company IS NOT NULL AND length(btrim(up.company)) > 0
GROUP BY up.company;

COMMENT ON VIEW public.company_leaderboard IS
  'Company-level aggregates. Companies with empty company field are excluded.';


-- =============================================================================
-- 14. RPC: mark_hadith_as_read
--
--     Single entry point for incrementing user progress. SECURITY DEFINER so
--     it can write user_stats / user_achievements regardless of caller's
--     direct table grants, but it ignores any client-supplied user id and
--     binds strictly to auth.uid().
--
--     Idempotent: calling twice for the same hadith returns already_read=true
--     with no point/coin reward.
--
--     Returns jsonb: {
--       already_read, new_points, new_coins,
--       total_hadiths, total_points, total_coins,
--       streak_days, level, trophies,
--       newly_unlocked: [achievement_id, ...]
--     }
-- =============================================================================
CREATE OR REPLACE FUNCTION public.mark_hadith_as_read(
  p_hadith_id bigint,
  p_source    text DEFAULT 'reader'
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public, pg_temp
AS $$
DECLARE
  v_user            uuid := auth.uid();
  v_role            text := auth.role();
  v_source          text;
  v_already_read    boolean := false;
  v_points_award    integer := 10;
  v_coins_award     integer := 5;
  v_today           date    := (now() AT TIME ZONE 'UTC')::date;
  v_last_date       date;
  v_prev_streak     integer;
  v_streak          integer;
  v_total_hadiths   integer;
  v_total_points    integer;
  v_total_coins     integer;
  v_level           integer;
  v_trophies        integer;
  v_hadith_exists   boolean;
  v_newly_unlocked  text[] := ARRAY[]::text[];
  v_ach             record;
BEGIN
  -- 1. Authenticated? Must satisfy BOTH:
  --    - auth.uid() returns a non-null user id (a real session)
  --    - auth.role() = 'authenticated' (rules out anon and service_role
  --      and prevents service_role from impersonating a user via
  --      manually-set JWT claims)
  IF v_user IS NULL OR v_role IS DISTINCT FROM 'authenticated' THEN
    RAISE EXCEPTION 'unauthorized: must be signed in as an authenticated user'
      USING ERRCODE = '42501';
  END IF;

  -- 2. Normalise the source. Allowlist only — anything unknown collapses
  --    to 'reader'. This keeps the source column reliable for analytics
  --    and prevents arbitrary strings being stored from the frontend.
  v_source := lower(btrim(COALESCE(p_source, 'reader')));
  IF v_source NOT IN ('reader','category','dashboard','competition','import') THEN
    v_source := 'reader';
  END IF;

  -- 3. Hadith exists?
  SELECT EXISTS (
    SELECT 1 FROM public.hadiths WHERE id = p_hadith_id
  ) INTO v_hadith_exists;

  IF NOT v_hadith_exists THEN
    RAISE EXCEPTION 'hadith % not found', p_hadith_id
      USING ERRCODE = 'P0002';
  END IF;

  -- 4. Lazy-create profile + stats rows.
  INSERT INTO public.user_profiles (id)
    VALUES (v_user)
    ON CONFLICT (id) DO NOTHING;

  INSERT INTO public.user_stats (user_id)
    VALUES (v_user)
    ON CONFLICT (user_id) DO NOTHING;

  -- 5. Insert progress; tell us whether it was a new read.
  WITH ins AS (
    INSERT INTO public.hadith_progress (user_id, hadith_id, source)
    VALUES (v_user, p_hadith_id, v_source)
    ON CONFLICT (user_id, hadith_id) DO NOTHING
    RETURNING 1
  )
  SELECT NOT EXISTS (SELECT 1 FROM ins) INTO v_already_read;

  -- 6. Idempotent path: already read → return current totals, no reward.
  IF v_already_read THEN
    SELECT hadiths_read, points, coins, trophies, streak_days, level
      INTO v_total_hadiths, v_total_points, v_total_coins,
           v_trophies, v_streak, v_level
      FROM public.user_stats
      WHERE user_id = v_user;

    RETURN jsonb_build_object(
      'already_read',   true,
      'new_points',     0,
      'new_coins',      0,
      'total_hadiths',  COALESCE(v_total_hadiths, 0),
      'total_points',   COALESCE(v_total_points, 0),
      'total_coins',    COALESCE(v_total_coins, 0),
      'streak_days',    COALESCE(v_streak, 0),
      'level',          COALESCE(v_level, 1),
      'trophies',       COALESCE(v_trophies, 0),
      'newly_unlocked', '[]'::jsonb
    );
  END IF;

  -- 7. New read — compute streak.
  SELECT last_read_date, streak_days
    INTO v_last_date, v_prev_streak
    FROM public.user_stats
    WHERE user_id = v_user;

  IF v_last_date IS NULL THEN
    v_streak := 1;
  ELSIF v_last_date = v_today THEN
    v_streak := COALESCE(v_prev_streak, 1);
  ELSIF v_last_date = (v_today - INTERVAL '1 day')::date THEN
    v_streak := COALESCE(v_prev_streak, 0) + 1;
  ELSE
    v_streak := 1;
  END IF;

  -- 8. Apply totals.
  UPDATE public.user_stats
     SET hadiths_read   = hadiths_read + 1,
         points         = points + v_points_award,
         coins          = coins  + v_coins_award,
         streak_days    = v_streak,
         last_read_date = v_today,
         level          = 1 + ((points + v_points_award) / 100),
         updated_at     = now()
   WHERE user_id = v_user
   RETURNING hadiths_read, points, coins, level
        INTO v_total_hadiths, v_total_points, v_total_coins, v_level;

  -- 9. Unlock any achievements whose threshold is now met.
  --    NOTE: clients cannot insert into user_achievements directly.
  --    This is the only code path that writes to that table.
  FOR v_ach IN
    SELECT a.id
      FROM public.achievements a
     WHERE NOT EXISTS (
             SELECT 1
               FROM public.user_achievements ua
              WHERE ua.user_id        = v_user
                AND ua.achievement_id = a.id
           )
       AND (
             (a.threshold_type = 'hadiths_read' AND v_total_hadiths >= a.threshold_value) OR
             (a.threshold_type = 'streak_days'  AND v_streak        >= a.threshold_value) OR
             (a.threshold_type = 'points'       AND v_total_points  >= a.threshold_value)
           )
  LOOP
    INSERT INTO public.user_achievements (user_id, achievement_id)
    VALUES (v_user, v_ach.id)
    ON CONFLICT (user_id, achievement_id) DO NOTHING;
    v_newly_unlocked := array_append(v_newly_unlocked, v_ach.id);
  END LOOP;

  -- 10. Update trophies count from the join table.
  SELECT COUNT(*)::integer
    INTO v_trophies
    FROM public.user_achievements
    WHERE user_id = v_user;

  UPDATE public.user_stats
     SET trophies = v_trophies,
         updated_at = now()
   WHERE user_id = v_user;

  -- 11. Return summary.
  RETURN jsonb_build_object(
    'already_read',   false,
    'new_points',     v_points_award,
    'new_coins',      v_coins_award,
    'total_hadiths',  v_total_hadiths,
    'total_points',   v_total_points,
    'total_coins',    v_total_coins,
    'streak_days',    v_streak,
    'level',          v_level,
    'trophies',       v_trophies,
    'newly_unlocked', to_jsonb(v_newly_unlocked)
  );
END;
$$;

