-- =============================================================================
-- Migration: 001_hadith_progress_dashboard.sql
-- Project:   Timeless Hadith
-- Purpose:   Add user progress, dashboard, leaderboard, and achievement tracking
--            on top of the existing public.hadiths table.
--
-- SAFETY CONTRACT
--   * Additive only — nothing is dropped or renamed.
--   * public.hadiths is NOT modified.
--   * No foreign key from hadith_progress.hadith_id to public.hadiths.id
--     (intentional — keeps reimports of hadith content non-destructive).
--   * Auth schema is NOT modified.
--   * Every user-owned table has RLS enabled and self-only write policies.
--   * Public hadith content remains readable by anon (existing behavior).
--   * The mark_hadith_as_read RPC ignores any client-supplied user id and
--     binds to auth.uid() inside the function. SECURITY DEFINER, search_path
--     pinned to public, pg_temp. Auth gate checks BOTH auth.uid() IS NOT NULL
--     and auth.role() = 'authenticated'.
--   * user_achievements is read-only to clients. No INSERT/UPDATE/DELETE
--     policies exist on the table — achievements can ONLY be unlocked by
--     the mark_hadith_as_read RPC (SECURITY DEFINER bypasses RLS).
--   * mark_hadith_as_read normalises p_source to one of:
--       reader | category | dashboard | competition | import
--     Anything else collapses to 'reader'.
--
-- USAGE
--   Run from Supabase SQL Editor as a single transaction.
--   Re-running is safe: every CREATE uses IF NOT EXISTS / OR REPLACE,
--   every policy uses DROP POLICY IF EXISTS before CREATE POLICY,
--   seed inserts use ON CONFLICT DO NOTHING.
-- =============================================================================

